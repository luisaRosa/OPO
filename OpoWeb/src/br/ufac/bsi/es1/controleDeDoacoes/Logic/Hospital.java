package br.ufac.bsi.es1.controleDeDoacoes.Logic;

public class Hospital {
	
	private int id;
	private String hospitalNotificador;
	private String hospitalOrigem;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getHospitalNotificador() {
		return hospitalNotificador;
	}
	public void setHospitalNotificador(String hospitalNotificador) {
		this.hospitalNotificador = hospitalNotificador;
	}
	public String getHospitalOrigem() {
		return hospitalOrigem;
	}
	public void setHospitalOrigem(String hospitalOrigem) {
		this.hospitalOrigem = hospitalOrigem;
	}
	
	

}
